$(document).ready(function(){
                
        // Iterate over each one of the measurement units
        $.each(values, function( index, measurementUnit ){
                
                // Append values as options to the selects
                $("#select-1").append(`<option value="${measurementUnit.m2}">${measurementUnit.descrizione}</option>`);
                $("#select-2").append(`<option value="${measurementUnit.m2}">${measurementUnit.descrizione}</option>`);
        });
        
        // Form the selects
        $('#select-1').formSelect();
        $('#select-2').formSelect();


        // Declare a function used to convert values
        var handleConversion = function(){

                // Retrieve input value
                var valueInStartingUnit = $("#input-1").val();
                
                // Retrieve starting measure in m2
                var startingMeasureInM2 = $("#select-1").children("option:selected").val();
                
                // Retrieve destination measure in m2
                var destinationMeasureInM2 = $("#select-2").children("option:selected").val();
                
                // Convert value into m2
                var inputValueConvertedToM2 = valueInStartingUnit * startingMeasureInM2;
                
                // Convert value into destination unit
                var outputValueInDestinationUnit = inputValueConvertedToM2 / destinationMeasureInM2;     
                
                // Round the number and keep just 5 decimal numbers
                var rounderOutputValue = Math.round(outputValueInDestinationUnit * 100000) / 100000
                
                $("#input-2").val(rounderOutputValue);
        };

        // Handle dropdown changes
        $("#select-1").change(handleConversion);
        $("#select-2").change(handleConversion);

        // Handle user input
        $('#input-1').on('input',handleConversion);

        $("#convert-btn").click(handleConversion); 
});